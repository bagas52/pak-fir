// Contoh tipe data angka dalam JavaScript
let angkaBulat = 58;            // Bilangan bulat
let angkaDecimal = 7.89;       // Bilangan desimal (float)
let hasilPerhitungan = angkaBulat + angkaDecimal; // Operasi penjumlahan

console.log("Angka Bulat:", angkaBulat);       // Output: 58
console.log("Angka Desimal:", angkaDecimal);    // Output: 7.89
console.log("Hasil Penjumlahan:", hasilPerhitungan); // Output: 65.89
