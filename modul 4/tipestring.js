// Contoh tipe data String dalam JavaScript
let namaDepan = 'RIZKI';           // Menggunakan tanda kutip satu (single quote)
let namaBelakang = "MAULUDDIN";          // Menggunakan tanda kutip ganda (double quote)
let kalimat = "Halo, nama saya " + namaDepan + " " + namaBelakang + "."; // Menggabungkan string

console.log(namaDepan);            // Output: RIZKI
console.log(namaBelakang);         // Output: MAULUDDIN
console.log(kalimat);              // Output: Halo, nama saya RIZKI MAULUDDIN.
